# Definizione delle costanti contenenti i due numeri interi
NUMERO_1 = 10
NUMERO_2 = 5

# Calcolo della somma
somma = NUMERO_1 + NUMERO_2
print("La somma è:", somma)
print("La somma è:", NUMERO_1 + NUMERO_2)

# Calcolo della differenza
differenza = NUMERO_1 - NUMERO_2
print("La differenza è:", differenza)

# Calcolo del prodotto
prodotto = NUMERO_1 * NUMERO_2
print("Il prodotto è:", prodotto)

# Calcolo del valore medio
valore_medio = (NUMERO_1 + NUMERO_2) / 2
print("Il valore medio è:", valore_medio)

valore_medio = (NUMERO_1 + NUMERO_2) // 2
print("Il valore medio è:", valore_medio)

# Calcolo della distanza
distanza = abs(NUMERO_1 - NUMERO_2)
print("La distanza è:", distanza)

# Calcolo del valore massimo
valore_massimo = max(NUMERO_1, NUMERO_2)
print("Il valore massimo è:", valore_massimo)

# Calcolo del valore minimo
valore_minimo = min(NUMERO_1, NUMERO_2)
print("Il valore minimo è:", valore_minimo)
